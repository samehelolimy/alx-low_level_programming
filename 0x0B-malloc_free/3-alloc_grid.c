/**
 * alloc_grid - returns a pointer to a 2 dimensional array
 * @width: first pa
 * @height: second pa
 * Return: success
*/
int **alloc_grid(int width, int height)
{
	if (width == 0 || height == 0)
		return (NULL);
	int i = 0;
	int j = 0;
	int **arr;

	for (; arr[i][j] != '\0';)
	{
		return (**arr);
	}
}
